    public void cargardatos(ArrayList<Datos> Listado) {
        DefaultTableModel Model = (DefaultTableModel) jb2.getModel();
        for (Datos p : Listado) {
            Object[] fila = {p.getId(), p.getNombres(), p.getApellidos(), p.getEdad(), p.getGenero(), p.getIdioma()};
            Model.addRow(fila);
        }
    }

