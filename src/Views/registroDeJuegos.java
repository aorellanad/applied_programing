    private void btagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btagregarActionPerformed
        // TODO add your handling code here:
        if (!validaVacios()) {
            String nombre = txtnombre.getText();
            String fechalan = txtfecha.getText();
            String precio = txtprecio.getText();
            String Apto;
            int tipo = cbtipo.getSelectedIndex();
            int[] genero = new int[6];
            String comentarios= txtaComentarios.getText();
            //Apto
            if (rdbSi.isSelected()) {
                Apto = "SI";
            } else {
                Apto = "NO";
            }
            //Genero
            if (chkaccion.isSelected()) {
                genero[0] = 1;
            } else {
                genero[0] = 0;
            }
            if (chkaventura.isSelected()) {
                genero[1] = 1;
            } else {
                genero[1] = 0;
            }
            if (chkshooter.isSelected()) {
                genero[2] = 1;
            } else {
                genero[2] = 0;
            }
            if (chkcarreras.isSelected()) {
                genero[3] = 1;
            } else {
                genero[3] = 0;
            }
            if (chkeducativo.isSelected()) {
                genero[4] = 1;
            } else {
                genero[4] = 0;
            }
            if (chkestrategia.isSelected()) {
                genero[5] = 1;
            } else {
                genero[5] = 0;
            }
            Datos p = new Datos(nombre, fechalan, precio, tipo,Apto, genero, comentarios);
            this.obj.Agregar(p);
            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "Datos incompletos");
        }
    }//GEN-LAST:event_btagregarActionPerformed
private void limpiar() {
        txtnombre.setText(" ");
        txtfecha.setText(" ");
        txtprecio.setText(" ");
        txtaComentarios.setText("");
        btgapto.clearSelection();
        cbtipo.setSelectedIndex(0);
        chkaccion.setSelected(false);
        chkaventura.setSelected(false);
        chkshooter.setSelected(false);
        chkeducativo.setSelected(false);
        chkcarreras.setSelected(false);
        chkestrategia.setSelected(false);
    }
    private void bteliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteliminarActionPerformed
        // TODO add your handling code here:
        String nombre = JOptionPane.showInputDialog("Ingrese el Numero del juego a eliminar: ");
        String fecha = JOptionPane.showInputDialog("Ingrese la Fecha de lanzamiento: ");
        int confirm = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar el registro: ");
        if (confirm == 0) {
            this.obj.EliminarxFecha(nombre, fecha);
        }
    }//GEN-LAST:event_bteliminarActionPerformed
private boolean validaVacios() {
        boolean vacio = false;
        if (txtnombre.getText().isEmpty()) {
            vacio = true;
        } else if (txtfecha.getText().isEmpty()) {
            vacio = true;
        } else if (cbtipo.getSelectedIndex() == 0) {
            vacio = true;
        } else if (txtprecio.getText().isEmpty()) {
            vacio = true;
        }else if (btgapto.equals(0)) {
            vacio = true;
        }
        return vacio;
    }
    private void btbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbuscarActionPerformed
        // TODO add your handling code here:
        Datos p;
        String nombre = JOptionPane.showInputDialog("Ingrese el nombre: ");
        String fecha = JOptionPane.showInputDialog("Ingrese La Fecha de lanzamiento: ");
        try{
            p = this.obj.BuscarxFecha(nombre, fecha);
            txtnombre.setText(p.getNombre());
            txtfecha.setText(p.getFechaLan());
            txtprecio.setText(p.getPrecio());
            txtaComentarios.setText(p.getComentarios());
            if (p.getApto().equals("SI")) {
                rdbSi.setSelected(true);
            } else {
                rdbNO.setSelected(true);
            }
            cbtipo.setSelectedIndex(p.getConsola());
            //check
            if (p.getGenero()[0] == 1) {
                chkaccion.setSelected(true);
            } else {
                chkaccion.setSelected(false);
            }
            if (p.getGenero()[1] == 1) {
                chkaventura.setSelected(true);
            } else {
                chkaventura.setSelected(false);
            }
            if (p.getGenero()[2] == 1) {
                chkshooter.setSelected(true);
            } else {
                chkshooter.setSelected(false);
            }
            if (p.getGenero()[3] == 1) {
                chkcarreras.setSelected(true);
            } else {
                chkcarreras.setSelected(false);
            }
            if (p.getGenero()[4] == 1) {
                chkeducativo.setSelected(true);
            } else {
                chkeducativo.setSelected(false);
            }
            if (p.getGenero()[5] == 1) {
                chkestrategia.setSelected(true);
            } else {
                chkestrategia.setSelected(false);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Ingrese bien los Datos.");
        }
    }//GEN-LAST:event_btbuscarActionPerformed

