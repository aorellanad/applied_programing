    public void cargardatos(listaJuegos listado) {
        DefaultTableModel modelo = (DefaultTableModel) tbdatos.getModel();

        for (Datos p : listado.getLista()) {
            String consola = "";
            String apto = "";
            String genero = "";

            switch (p.getConsola()) {
                case 0:
                    consola = "";
                    break;
                case 1:
                    consola = "Ps3";
                    break;
                case 2:
                    consola = "Ps4";
                    break;
                case 3:
                    consola = "Xbox One";
                    break;
                case 4:
                    consola = "Pc";
                    break;
                case 5:
                    consola = "Otro";
                    break;
            }

            if (p.getGenero()[0] == 1) {
                genero = " Accion";
            }
            if (p.getGenero()[1] == 1) {
                genero = genero + "; Aventura";
            }
            if (p.getGenero()[2] == 1) {
                genero = genero + "; Shooters";
            }
            if (p.getGenero()[3] == 1) {
                genero = genero + "; Carreras";
            }
            if (p.getGenero()[4] == 1) {
                genero = genero + "; Educativo";
            }
            if (p.getGenero()[5] == 1) {
                genero = genero + "; Estrategia";
            }

            if (p.getApto().equals("SI")) {
                apto = "SI";
            } else {
                apto = "NO";
            }
            Object[] fila = {p.getNombre(),
                p.getFechaLan(),
                p.getPrecio(),
                consola, apto, genero};
            modelo.addRow(fila);
        }
    }

