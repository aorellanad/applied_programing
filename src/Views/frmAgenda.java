    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        Agenda p;
        String nombre = JOptionPane.showInputDialog("Ingrese el nombre de la actividad");
        try {
            p = this.listado.Buscarxnombre(nombre);
            txtactividad.setText(p.getNombre());
            txthinicio.setText(p.getH_inicio());
            txthfinal.setText(p.getH_final());
            if (p.getActividad().equals("Realizada")) {
                rbrealizada.setSelected(true);
            } else {
                rbnorealizada.setSelected(true);
            }
            cbtactividad.setSelectedIndex(p.getT_ac());
            if (p.getDia_actividad()[0] == 1) {
                chklunes.setSelected(true);
            } else {
                chklunes.setSelected(false);
            }
            if (p.getDia_actividad()[1] == 1) {
                chkmartes.setSelected(true);
            } else {
                chkmartes.setSelected(false);
            }
            if (p.getDia_actividad()[2] == 1) {
                chkmiercoles.setSelected(true);
            } else {
                chkmiercoles.setSelected(false);
            }
            if (p.getDia_actividad()[3] == 1) {
                chkjueves.setSelected(true);
            } else {
                chkjueves.setSelected(false);
            }
            if (p.getDia_actividad()[4] == 1) {
                chkviernes.setSelected(true);
            } else {
                chkviernes.setSelected(false);
            }
            if (p.getDia_actividad()[5] == 1) {
                chksabado.setSelected(true);
            } else {
                chksabado.setSelected(false);
            }
            if (p.getDia_actividad()[6] == 1) {
                chkdomingo.setSelected(true);
            } else {
                chkdomingo.setSelected(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Registro no encontrado...");
            limpiar();
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

