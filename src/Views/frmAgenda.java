    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String nombre = txtactividad.getText();
        String hinicio = txthinicio.getText();
        String hfinal = txthfinal.getText();
        String actividad;
        if (rbrealizada.isSelected()) {
            actividad = "Realizada";
        } else {
            actividad = "No Realizada";
        }
        int t_ac = cbtactividad.getSelectedIndex();
        int[] dia_actividad = new int[7];
        if (chklunes.isSelected()) {
            dia_actividad[0] = 1;
        } else {
            dia_actividad[0] = 0;
        }
        if (chkmartes.isSelected()) {
            dia_actividad[1] = 1;
        } else {
            dia_actividad[1] = 0;
        }
        if (chkmiercoles.isSelected()) {
            dia_actividad[2] = 1;
        } else {
            dia_actividad[2] = 0;
        }
        if (chkjueves.isSelected()) {
            dia_actividad[3] = 1;
        } else {
            dia_actividad[3] = 0;
        }
        if (chkviernes.isSelected()) {
            dia_actividad[4] = 1;
        } else {
            dia_actividad[4] = 0;
        }
        if (chksabado.isSelected()) {
            dia_actividad[5] = 1;
        } else {
            dia_actividad[5] = 0;
        }
        if (chkdomingo.isSelected()) {
            dia_actividad[6] = 1;
        } else {
            dia_actividad[6] = 0;
        }
        Agenda p = new Agenda(nombre, t_ac, hinicio, hfinal, actividad, dia_actividad);
        this.listado.Actualizarxnombre(p);
        limpiar();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String nombre = JOptionPane.showInputDialog("Ingrese el nombre de la actividad");
        int confirm = JOptionPane.showConfirmDialog(this, "Seguro que quiere eliminar esta actividad");
        if (confirm == 0) {
            this.listado.Eliminarxnombre(nombre);
        }
        limpiar();
    }//GEN-LAST:event_btnRemoveActionPerformed

