    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String codigo =JOptionPane.showInputDialog("ingrese el codigo del producto a eliminar");
        int confirm = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar el registro");
        if(confirm==0)
        this.listado.Eliminarxcedula(codigo);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Producto p;
        String codigo = JOptionPane.showInputDialog("ingrese el codigo");
        try {
            p = this.listado.Buscarxcog(codigo);
            txtcodigo.setText(p.getCodigo());
            txtdescripcion.setText(p.getDescripcion());
            txtprecio.setText(p.getPrecio()+"");
            txtStock.setText(p.getStock()+"");
            txtelab.setText(p.getFechaElab());
            txtexp.setText(p.getFechaExp());
            if (p.getImportado().equals("SI")) {
                rdbSI.setSelected(true);
            } else {
                rdbNO.setSelected(true);
            }
            combtipo.setSelectedIndex(p.getTipo());
 
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Registro no encontrado...");
            limpiar();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

