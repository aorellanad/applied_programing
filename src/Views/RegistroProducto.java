    private void limpiar(){
        txtcodigo.setText("");
        txtdescripcion.setText("");
        txtprecio.setText("");
        txtStock.setText("");
        rdbSI.setSelected(true);
        combtipo.setSelectedIndex(0);
        txtelab.setText("");
        txtexp.setText("");    
    }
    private boolean validaVacios(){
        boolean vacio=false;
        if(txtcodigo.getText().equals("")){
          vacio = true;  
        }
        else if(txtdescripcion.getText().equals("")){
          vacio = true;  
        }
        else if(txtprecio.getText().equals("")){
          vacio = true;  
        }
        else if(txtStock.getText().equals("")){
          vacio = true;  
        }
        else if(txtelab.getText().equals("")){
          vacio = true;  
        }
        else if(txtexp.getText().equals("")){
          vacio = true;  
        }
        return vacio;
    }
