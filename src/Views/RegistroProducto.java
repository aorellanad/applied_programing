    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!validaVacios()) {
            String codigo = txtcodigo.getText();
            String descripcion = txtdescripcion.getText();
            float precio = Float.parseFloat(txtprecio.getText());
            int stock = Integer.parseInt(txtStock.getText());
            String importado;
            int tipo = combtipo.getSelectedIndex();
            String fechaelab = txtelab.getText();
            String fechaexp = txtexp.getText();
            if (rdbSI.isSelected()) {
                importado = "SI";
            } else {
                importado = "NO";
            }
            Producto p = new Producto(codigo, descripcion, precio, stock, importado, tipo, fechaelab, fechaexp);
            this.listado.Agregar(p);
            limpiar(); 
        }else {
            JOptionPane.showMessageDialog(null, "Datos incompletos ");
        }                                      
    }//GEN-LAST:event_btnAgregarActionPerformed

